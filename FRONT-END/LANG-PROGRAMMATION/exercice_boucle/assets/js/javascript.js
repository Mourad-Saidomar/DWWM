// Affichier la table de multiplication de 5
for (let i = 1; i <= 10; i++) {
    let resultat = 5 * i;    
    console.log(`5 x ${i} = ${resultat}`);    
}

// Compter à l'envers de 100 à 0
for (let i = 100; i >= 0; i--) {
    console.log(i);
}

// Compter de 0 à 100 entre 20 et 40 ou 70 et 80
for (let i = 0; i <= 100; i++) {
    if ((i >= 20 && i <= 40) || (i >= 70 && i <= 80)) {
        console.log(i);
    }
}




